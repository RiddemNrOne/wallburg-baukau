                        Leben: Adel, Gefolge, Handwerk und Gemeinschaft stehen dabei
                        im Mittelpunkt – inspiriert von den Menschen, die an Orten wie der Wallburg oder
                        Strünkede über Jahrhunderte hinweg lebten und wirkten.
                    </p>

